local TodoActions = {}

-- ============================================================================
-- Types
-- ============================================================================

TodoActions.Types = {
  TODO_ADDED = "todos/todoAdded"
}

-- ============================================================================
-- Action Creators
-- ============================================================================

--- Adds a new todo.
--- @param text string
--- @return WuxAction
function TodoActions:AddTodo(text)
  return { type = TodoActions.Types.TODO_ADDED, payload = text }
end

-- ============================================================================
-- Return
-- ============================================================================

return TodoActions
